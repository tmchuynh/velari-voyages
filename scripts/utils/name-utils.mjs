/**
 * Get a random name
 * @param {boolean} useFeminineName - If true, uses a feminine name, otherwise uses a masculine name
 * @returns {string} Full name (first and last)
 */
export function getRandomName(useFeminineName = Math.random() > 0.5) {
  const firstName = useFeminineName
    ? feminineNames[Math.floor(Math.random() * feminineNames.length)]
    : masculineNames[Math.floor(Math.random() * masculineNames.length)];

  const lastName = lastNames[Math.floor(Math.random() * lastNames.length)];

  return `${firstName} ${lastName}`;
}

/**
 * Determine gender from first name
 * @param {string} firstName - First name to check
 * @returns {string} "men" or "women" for use with profile image API
 */
export function determineGenderFromName(firstName) {
  if (masculineNames.includes(firstName)) {
    return "men";
  } else if (feminineNames.includes(firstName)) {
    return "women";
  }

  // Default to first letter as a simple heuristic if not found in our lists
  return firstName.charAt(0).toUpperCase() <= "M" ? "men" : "women";
}

