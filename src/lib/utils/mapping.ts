export function getCityCountryPriceMultiplier(country: string): number {
  const currency: string =
    currenciesMap.find((c) => c.country.toLowerCase() === country.toLowerCase())
      ?.code || "USD"; // Default to USD if not found
  if (!currency) {
    console.warn(`No currency found for country: ${country}`);
    return 1; // Default to USD if not found
  }
  const rate: number = currencyRates[currency] || 1;
  return rate;
}

